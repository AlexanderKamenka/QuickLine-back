package my.app.quickline.service;

import com.twilio.rest.api.v2010.account.Message;
import com.twilio.type.PhoneNumber;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Service;

import java.security.SecureRandom;
import java.time.LocalDateTime;
import java.time.temporal.ChronoUnit;
import java.util.Map;
import java.util.concurrent.ConcurrentHashMap;

@Service
public class TwilioVerificationService {

    @Value("${twilio.whatsapp.from}")
    private String twilioWhatsAppNumber;

    @Value("${twilio.dev.mode:true}")
    private boolean devMode;

    private static final int CODE_LENGTH = 6;
    private static final int CODE_EXPIRY_MINUTES = 5;
    private static final int MAX_ATTEMPTS = 3;
    private static final int RESEND_COOLDOWN_SECONDS = 60;

    private Map<String, VerificationCode> verificationCodes = new ConcurrentHashMap<>();

    /**
     * –í–Ω—É—Ç—Ä–µ–Ω–Ω–∏–π –∫–ª–∞—Å—Å –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∫–æ–¥–∞ –≤–µ—Ä–∏—Ñ–∏–∫–∞—Ü–∏–∏
     */
    private static class VerificationCode {
        String code;
        LocalDateTime expiryTime;
        LocalDateTime lastSentTime;
        int attempts;

        VerificationCode(String code) {
            this.code = code;
            this.expiryTime = LocalDateTime.now().plusMinutes(CODE_EXPIRY_MINUTES);
            this.lastSentTime = LocalDateTime.now();
            this.attempts = 0;
        }

        boolean isExpired() {
            return LocalDateTime.now().isAfter(expiryTime);
        }

        boolean canResend() {
            long secondsSinceLastSend = ChronoUnit.SECONDS.between(lastSentTime, LocalDateTime.now());
            return secondsSinceLastSend >= RESEND_COOLDOWN_SECONDS;
        }

        void updateLastSentTime() {
            this.lastSentTime = LocalDateTime.now();
        }

        void incrementAttempts() {
            this.attempts++;
        }

        boolean isTooManyAttempts() {
            return attempts >= MAX_ATTEMPTS;
        }

        long getRemainingSeconds() {
            return CODE_EXPIRY_MINUTES * 60 - ChronoUnit.SECONDS.between(
                    expiryTime.minusMinutes(CODE_EXPIRY_MINUTES),
                    LocalDateTime.now()
            );
        }
    }

    /**
     * –û—Ç–ø—Ä–∞–≤–∏—Ç—å –∫–æ–¥ –≤–µ—Ä–∏—Ñ–∏–∫–∞—Ü–∏–∏ —á–µ—Ä–µ–∑ WhatsApp
     */
    public Map<String, Object> sendVerificationCode(String phoneNumber) {
        cleanExpiredCodes();

        // –ù–æ—Ä–º–∞–ª–∏–∑—É–µ–º –Ω–æ–º–µ—Ä —Ç–µ–ª–µ—Ñ–æ–Ω–∞
        String normalizedPhone = normalizePhoneNumber(phoneNumber);

        // –ü—Ä–æ–≤–µ—Ä—è–µ–º —Å—É—â–µ—Å—Ç–≤—É—é—â–∏–π –∫–æ–¥
        VerificationCode existingCode = verificationCodes.get(normalizedPhone);
        if (existingCode != null && !existingCode.isExpired()) {
            if (!existingCode.canResend()) {
                long waitTime = RESEND_COOLDOWN_SECONDS -
                        ChronoUnit.SECONDS.between(existingCode.lastSentTime, LocalDateTime.now());
                return Map.of(
                        "success", false,
                        "error", "Please wait " + waitTime + " seconds before requesting a new code"
                );
            }
        }

        // –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –Ω–æ–≤—ã–π –∫–æ–¥
        String code = generateCode();
        VerificationCode verificationCode = new VerificationCode(code);
        verificationCodes.put(normalizedPhone, verificationCode);

        // –§–æ—Ä–º–∏—Ä—É–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ
        String message = String.format(
                "üîê *–ö–æ–¥ –≤–µ—Ä–∏—Ñ–∏–∫–∞—Ü–∏–∏ Beauty Salon*\n\n" +
                        "–í–∞—à –∫–æ–¥: *%s*\n\n" +
                        "–ö–æ–¥ –¥–µ–π—Å—Ç–≤–∏—Ç–µ–ª–µ–Ω %d –º–∏–Ω—É—Ç.\n\n" +
                        "–ï—Å–ª–∏ –≤—ã –Ω–µ –∑–∞–ø—Ä–∞—à–∏–≤–∞–ª–∏ –∫–æ–¥, –ø—Ä–æ–∏–≥–Ω–æ—Ä–∏—Ä—É–π—Ç–µ —ç—Ç–æ —Å–æ–æ–±—â–µ–Ω–∏–µ.",
                code, CODE_EXPIRY_MINUTES
        );

        // DEV MODE - –≤—ã–≤–æ–¥ –≤ –∫–æ–Ω—Å–æ–ª—å
        if (devMode) {
            printDevModeCode(normalizedPhone, code, verificationCode.getRemainingSeconds());
            return Map.of(
                    "success", true,
                    "message", "Code sent (DEV MODE - check console)",
                    "expiresIn", CODE_EXPIRY_MINUTES * 60
            );
        }

        // PRODUCTION MODE - —Ä–µ–∞–ª—å–Ω–∞—è –æ—Ç–ø—Ä–∞–≤–∫–∞ —á–µ—Ä–µ–∑ Twilio
        try {
            sendWhatsAppMessage(normalizedPhone, message);

            System.out.println("‚úÖ WhatsApp code sent via Twilio to: " + normalizedPhone);

            return Map.of(
                    "success", true,
                    "message", "Verification code sent to WhatsApp",
                    "expiresIn", CODE_EXPIRY_MINUTES * 60
            );
        } catch (Exception e) {
            System.err.println("‚ùå Twilio error: " + e.getMessage());
            e.printStackTrace();

            // Fallback –≤ –∫–æ–Ω—Å–æ–ª—å –ø—Ä–∏ –æ—à–∏–±–∫–µ
            printDevModeCode(normalizedPhone, code, verificationCode.getRemainingSeconds());

            return Map.of(
                    "success", false,
                    "error", "Failed to send WhatsApp message. Code printed to console (DEV FALLBACK)"
            );
        }
    }

    /**
     * –û—Ç–ø—Ä–∞–≤–∫–∞ —Å–æ–æ–±—â–µ–Ω–∏—è —á–µ—Ä–µ–∑ Twilio WhatsApp API
     */
    private void sendWhatsAppMessage(String phoneNumber, String messageText) {
        try {
            Message message = Message.creator(
                    new PhoneNumber("whatsapp:" + phoneNumber),
                    new PhoneNumber(twilioWhatsAppNumber),
                    messageText
            ).create();

            System.out.println("üì§ Twilio Message SID: " + message.getSid());
            System.out.println("üìä Status: " + message.getStatus());
        } catch (Exception e) {
            System.err.println("‚ùå Twilio API Error: " + e.getMessage());
            throw new RuntimeException("Failed to send WhatsApp via Twilio: " + e.getMessage(), e);
        }
    }

    /**
     * –ü—Ä–æ–≤–µ—Ä–∫–∞ –≤–≤–µ–¥–µ–Ω–Ω–æ–≥–æ –∫–æ–¥–∞
     */
    public Map<String, Object> verifyCode(String phoneNumber, String code) {
        String normalizedPhone = normalizePhoneNumber(phoneNumber);
        VerificationCode storedCode = verificationCodes.get(normalizedPhone);

        if (storedCode == null) {
            System.out.println("‚ùå No code found for: " + normalizedPhone);
            return Map.of(
                    "verified", false,
                    "error", "No verification code found. Please request a new code."
            );
        }

        if (storedCode.isExpired()) {
            System.out.println("‚ùå Code expired for: " + normalizedPhone);
            verificationCodes.remove(normalizedPhone);
            return Map.of(
                    "verified", false,
                    "error", "Verification code has expired. Please request a new code."
            );
        }

        storedCode.incrementAttempts();

        if (storedCode.isTooManyAttempts()) {
            System.out.println("‚ùå Too many attempts for: " + normalizedPhone);
            verificationCodes.remove(normalizedPhone);
            return Map.of(
                    "verified", false,
                    "error", "Too many incorrect attempts. Please request a new code."
            );
        }

        if (storedCode.code.equals(code)) {
            System.out.println("‚úÖ Code verified successfully for: " + normalizedPhone);
            verificationCodes.remove(normalizedPhone);
            return Map.of(
                    "verified", true,
                    "message", "Phone number verified successfully"
            );
        }

        int remainingAttempts = MAX_ATTEMPTS - storedCode.attempts;
        System.out.println("‚ùå Invalid code for: " + normalizedPhone +
                " (attempts: " + storedCode.attempts + "/" + MAX_ATTEMPTS + ")");

        return Map.of(
                "verified", false,
                "error", "Invalid verification code. " + remainingAttempts + " attempts remaining."
        );
    }

    /**
     * –ù–æ—Ä–º–∞–ª–∏–∑–∞—Ü–∏—è –Ω–æ–º–µ—Ä–∞ —Ç–µ–ª–µ—Ñ–æ–Ω–∞
     */
    private String normalizePhoneNumber(String phoneNumber) {
        // –£–±–∏—Ä–∞–µ–º –ø—Ä–æ–±–µ–ª—ã, –¥–µ—Ñ–∏—Å—ã, —Å–∫–æ–±–∫–∏
        String normalized = phoneNumber.replaceAll("[\\s\\-\\(\\)]", "");

        // –ï—Å–ª–∏ –Ω–µ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å +, –¥–æ–±–∞–≤–ª—è–µ–º
        if (!normalized.startsWith("+")) {
            normalized = "+" + normalized;
        }

        return normalized;
    }

    /**
     * –ì–µ–Ω–µ—Ä–∞—Ü–∏—è —Å–ª—É—á–∞–π–Ω–æ–≥–æ 6-–∑–Ω–∞—á–Ω–æ–≥–æ –∫–æ–¥–∞
     */
    private String generateCode() {
        SecureRandom random = new SecureRandom();
        int code = random.nextInt(999999);
        return String.format("%06d", code);
    }

    /**
     * –û—á–∏—Å—Ç–∫–∞ –∏—Å—Ç–µ–∫—à–∏—Ö –∫–æ–¥–æ–≤
     */
    private void cleanExpiredCodes() {
        verificationCodes.entrySet().removeIf(entry -> entry.getValue().isExpired());
    }

    /**
     * –í—ã–≤–æ–¥ –∫–æ–¥–∞ –≤ –∫–æ–Ω—Å–æ–ª—å –¥–ª—è DEV MODE
     */
    private void printDevModeCode(String phone, String code, long expiresInSeconds) {
        System.out.println("\n‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó");
        System.out.println("‚ïë        üì± VERIFICATION CODE (DEV MODE)            ‚ïë");
        System.out.println("‚ï†‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ï£");
        System.out.println("‚ïë  Phone:    " + String.format("%-37s", phone) + "‚ïë");
        System.out.println("‚ïë  Code:     " + String.format("%-37s", code) + "‚ïë");
        System.out.println("‚ïë  Expires:  " + String.format("%-37s", expiresInSeconds + " seconds") + "‚ïë");
        System.out.println("‚ïö‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïù\n");
    }

    /**
     * –ü–æ–ª—É—á–∏—Ç—å —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –¥–ª—è –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞
     */
    public Map<String, Object> getStats() {
        cleanExpiredCodes();
        return Map.of(
                "service", "Twilio WhatsApp Verification",
                "devMode", devMode,
                "activeCodes", verificationCodes.size(),
                "codeExpiryMinutes", CODE_EXPIRY_MINUTES,
                "maxAttempts", MAX_ATTEMPTS
        );
    }
}